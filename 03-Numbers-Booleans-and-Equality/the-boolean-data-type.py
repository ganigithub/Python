#Equality operator ==
print('5' == '5')
print('hi' == 5) # string is not equal to integer
print(5 == 5.0)  # float and integer can be compared

#case sensitivity
print('hi' == 'hi')
print('hi' == 'Hi')

print(True == True)
print(False == False)
print(False == True)

#inequality operator !=
print(5 != 5)
print(5 != 6)
print('hi' != 'hi')
print('yes' != 'no')
print('music' != 'noise')
print(4.0 != 4)