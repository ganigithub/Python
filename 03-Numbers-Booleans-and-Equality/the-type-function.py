#Type function : tells what class or blueprint an object belongs to
print(type(2))
print(type(9))

print(type(2.0))
print(type(9.3))

print(type('computer'))
print(type('laptop'))

print(type(True))
print(type(False))

print(type(2) == type (9))
print(type(2.0) == type(4))
print(type('hello') == type(23))
print(type('computer') == type('laptop'))

print(type(3 <= 3))