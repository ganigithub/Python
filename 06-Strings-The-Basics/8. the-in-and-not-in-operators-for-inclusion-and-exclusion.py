# 8. in and not in Operators:
sentence = 'My name is gani.'
# the in operator checks whether a string is in the original string or not. Return is a bool
print('gani' in sentence) 
print('Gani' in sentence)
print(' ' in sentence)
print('garry' in sentence)

print()

#the not in operator checks whther a string is not in original string
print('gani' not in sentence)
print('Gani' not in sentence)
print('garry' not in sentence)
print(' ' not in sentence)